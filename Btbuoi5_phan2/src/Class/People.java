package Class;

public class People {
	String ten;
	int tuoi;
	String cCCD;
	
	public People(String ten, int tuoi, String cCCD) {
		super();
		this.ten = ten;
		this.tuoi = tuoi;
		this.cCCD = cCCD;
	}
	
	public String getTen() {
		return ten;
	}
	public void setTen(String ten) {
		this.ten = ten;
	}
	public int getTuoi() {
		return tuoi;
	}
	public void setTuoi(int tuoi) {
		this.tuoi = tuoi;
	}
	public String getcCCD() {
		return cCCD;
	}
	public void setcCCD(String cCCD) {
		this.cCCD = cCCD;
	}
	
	
}
